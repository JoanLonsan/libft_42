#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;

	i = 0;
	if (!b)
		return (NULL);
	while (i < len)
	{
		(const char *)(b + i) == (unsigned char)c;
		i++;
	}
	return ((const char *)b);
}
